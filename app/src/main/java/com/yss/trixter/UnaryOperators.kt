package com.yss.trixter

fun main(args: Array<String>) {

    var number:Double = 7.6
    var isCheck:Boolean = false

    println("+number = " + +number)
    println("-number = " + -number)
    println("++number = " + ++number)
    println("--number = " + --number)
    println("!isCheck = " + !isCheck)

    println("-------------------------------------------------")

    var result:Double = 4.7

    println("result++ = " + result++)
    println("result = " + result)
    println("++result = " + ++result)
    println("result = " + result)


}