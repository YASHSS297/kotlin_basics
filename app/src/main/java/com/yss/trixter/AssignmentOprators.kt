package com.yss.trixter

fun main(args: Array<String>) {

    var x:Int = 32
    var y:Int = 6
    var z:Int = 0

    z = x + y
    println("z = " + z)

    z += x
    println("z +=x = " + z)

    z -= x
    println("z -=x = " + z)

    z *= x
    println("z *=x = " + z)

    z /= x
    println("z /=x = " + z)

    z %= x
    println("z %=x = " + z)






}